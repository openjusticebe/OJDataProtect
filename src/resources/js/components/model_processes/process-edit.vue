    <h2 class="text-2xl">Legal bases</h2>
    <ul>
      <li>
        <strong>Consent</strong> The data subject has given permission for the
        organisation to process their personal data for one or more processing
        activities. Consent must be freely given, clear, and easy to withdraw,
        so organisations need to be careful when using consent as their legal
        basis. For example, the age of automatically-checked consent boxes is
        coming to an end through GDPR.
      </li>

      <li>
        <strong>Performance of a Contract</strong> The data processing activity
        is necessary to enter into or perform a contract with the data subject.
        If the processing activity does not relate to the terms of the contract,
        then that data processing activity needs to be covered by a different
        legal basis.
      </li>

      <li>
        <strong>Legitimate Interest</strong> This is a processing activity that
        a data subject would normally expect from an organisation that it gives
        its personal data to do, like marketing activities and fraud prevention.
        If legitimate interest is used as a legal basis for processing, the
        organisation must perform a balancing test: is this processing activity
        necessary for the organisation to function? Does the processing activity
        outweigh any risks to a data subject’s rights and freedoms? If the
        answer to either of those questions is “no,” then the organisation
        cannot use legitimate interest as its legal basis for processing.
      </li>
      <li>
        <strong>Vital Interest</strong> A rare processing activity that could be
        required to save someone’s life. This is most commonly seen in emergency
        medical care situations.
      </li>
      <li>
        <strong> Legal Requirement</strong> The processing activity is necessary
        for a legal obligation, such as an information security, employment or
        consumer transaction law.
      </li>
      <li>
        <strong>Public Interest</strong> A processing activity that would occur
        by a government entity or an organisation acting on behalf of a
        government entity.
      </li>
    </ul>
